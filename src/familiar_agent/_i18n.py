        ("ml", "ml"),
        ("pa", "pa"),
        ("ur", "ur"),
        ("sw", "sw"),
        ("af", "af"),
        ("sv", "sv"),
        ("no", "no"),
        ("da", "da"),
        ("fi", "fi"),
        ("cs", "cs"),
        ("sk", "sk"),
